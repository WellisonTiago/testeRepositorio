    private void exibirMensagemErro(String mensagem) {
        JOptionPane.showMessageDialog(this, mensagem, "Erro", JOptionPane.ERROR_MESSAGE);
    }

    private void exibirMensagemSucesso(String mensagem) {
        JOptionPane.showMessageDialog(this, mensagem, "Cadastro de Paciente", JOptionPane.INFORMATION_MESSAGE);
    }

    private void carregarListaPaciente() throws SQLException {
        PacienteBO pacienteBO = new PacienteBO();
        pacientes = pacienteBO.buscarTodos();
    }
    
    private void Excluir(String CPF) throws SQLException {
        PacienteBO pacienteBO = new PacienteBO();
        
       pacientes = pacienteBO.delete(CPF);
    }

    class ModeloDadosPaciente extends AbstractTableModel {

        @Override

        public int getRowCount() {

            return pacientes.size();
        }

        @Override
        public int getColumnCount() {
            return 4;
        }

        @Override
        public Object getValueAt(int linha, int coluna) {
            Paciente paciente = pacientes.get(linha);
            //Funcionario funcionario = funcionarios.get(linha);

            if (coluna == 0) {
                return paciente.getCPF();
            } else if (coluna == 1) {
                return paciente.getNome();
            } else if (coluna == 2) {
                return paciente.getIdade();
            } else  {
                return paciente.getSalario();
            }
            

        }
    }

    private void exibirDadosTabela() {
        try {
            this.carregarListaPaciente();
            ModeloDadosPaciente modelo = new ModeloDadosPaciente();
            tbFuncionarios.setModel(modelo);
        } catch (Exception ex) {
            Logger.getLogger(TelaPaciente.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
    private void btAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdicionarActionPerformed
        // TODO add your handling code here:

        try {
            lerDadosTela();
        } catch (Exception ex) {
            Logger.getLogger(TelaPaciente.class.getName()).log(Level.SEVERE, null, ex);
        }

        PacienteBO pacienteBO = new PacienteBO();
        try {
            pacienteBO.criar(paciente);

            System.out.println("Paceinte cadastrado com sucesso!");
            String mensagem = "Paceinte cadastrado com sucesso!";
            String titulo = null;
            JOptionPane.showMessageDialog(this, mensagem, titulo, JOptionPane.INFORMATION_MESSAGE);
            this.exibirDadosTabela();
            this.limparCamposTela();
        } catch (SQLException ex) {
            Logger.getLogger(TelaPaciente.class
                    .getName()).log(Level.SEVERE, null, ex);
        } catch (PacienteSalarioException ex) {
            String mensagem = "Erro ao cadastrar Paciente! Salario Insuficiente.";
            this.exibirMensagemErro(mensagem);
            Logger.getLogger(TelaPaciente.class.getName()).log(Level.SEVERE, null, ex);
        } catch (PacienteIdadeInsuficienteException ex) {
            String mensagem = "Erro ao cadastrar paciente! Não atendemos Crianças nem Idosos.";
            this.exibirMensagemErro(mensagem);
            Logger.getLogger(TelaPaciente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btAdicionarActionPerformed

