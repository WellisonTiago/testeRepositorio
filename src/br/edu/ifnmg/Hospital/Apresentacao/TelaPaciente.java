    private void lerDadosTela() throws ParseException {

        paciente = new Paciente();

        paciente.setNome(txtNome.getText().trim());
        paciente.setCPF(txtCPF.getText().trim());

        String Idade = txtIdade.getText().trim();
        paciente.setIdade(Integer.parseInt(Idade));

        String salario = txtSalario.getText().trim();
        DecimalFormat formatador = new DecimalFormat("#,##0.00");

        paciente.setSalario((float) formatador.parse(salario).doubleValue());

    }

    private void limparCamposTela() {

        this.txtCPF.setText("");
        this.txtNome.setText("");
        this.txtIdade.setText("");
        this.txtSalario.setText("");

    }

